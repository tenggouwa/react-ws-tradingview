// 邮箱校验
export const EMAIL_REG = /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/;
// 长度大于6位包含小写字母和数字的密码校验
export const PWD_REG = /^\S*(?=\S{6,})(?=\S*\d)(?=\S*[a-z])\S*$/;
// 长度8-16位包含大小写和数字的效验
export const BIND_PWD = /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)[^]{8,16}$/;
// 纯数字校验
export const NUMBER_REG = /^[1-9]\d*$/;
// 输入字母或数字
export const REPLACE_REG = /^[A-Za-z0-9]*$/;